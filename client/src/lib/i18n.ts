export type TranslationKey = keyof typeof T.en;

export const LangContext = createContext<{
  lang: Lang;
  setLang: (l: Lang) => void;
}>({ lang: 'en', setLang: () => {} });

export function useLang() {
  return useContext(LangContext);
}

export function useT() {
  const { lang } = useLang();
  return (key: TranslationKey) => (T[lang] as Record<string, string>)[key] ?? (T.en as Record<string, string>)[key] ?? key;
}
